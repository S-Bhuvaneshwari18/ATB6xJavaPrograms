package ex_04052024;

public class Lab141 {

	public static void main(String[] args) {
		// For each 
      int[] arr={23,45,56};
      for (int item:arr) {
    	  System.out.println(item);
      }
      System.out.println("--------");
      
      for (int i=0; i<arr.length; i++) {
    	  System.out.println(arr[i]);
      }
	}

}
