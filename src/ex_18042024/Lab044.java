package ex_18042024;

public class Lab044 {

	public static void main(String[] args) {
		// Increment(++)/ Decrement(--) Operators
		
		//pre ,post
		
		//pre --> Value Incremented first and then the result is computed 
		 int a = 10;
		 // ++a -->a=a+1 (Value   Increase first --> assign the Value)
		 int b =  ++a;
		  System.out.println(b);
		  System.out.println(a);//a  Value will also increase
		 

	}

}
