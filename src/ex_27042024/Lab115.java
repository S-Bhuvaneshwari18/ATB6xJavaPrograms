package ex_27042024;

public class Lab115 {

	public static void main(String[] args) {
		// while loop
		//--> Initialization
		//B-->Condition
		//C-->Increment
		
		int i=1;//A
		while(i<=10) { //B
			//Execute the code
			 System.out.println(i);
			 i++; //C
		}

		for(int j=1 ; j<=10 ;j++) { //1 to 10
			 System.out.println(j);
		}
	}

}
